package com.ditzms.fireandice.data.network;

import retrofit2.Retrofit;
import retrofit2.converter.gson.GsonConverterFactory;

/**
 * Created by Dragos-Daniel Dit on 9/24/2016.
 */
public class FireAndIceClient {

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // 
    //  Fields
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////

    private FireAndIceApi mFireAndIceApi;

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // 
    //  Constructor
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////

    public FireAndIceClient() {
        Retrofit retrofit = new Retrofit.Builder()
                .baseUrl("https://anapioficeandfire.com/api/")
                .addConverterFactory(GsonConverterFactory.create())
                .build();

        mFireAndIceApi = retrofit.create(FireAndIceApi.class);
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // 
    //  Public methods
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////

    public FireAndIceApi getFireAndIceApi() {
        return mFireAndIceApi;
    }
}
